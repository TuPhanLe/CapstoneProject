void uart_init(void)
{
    esp_log_level_set(TAG, ESP_LOG_INFO);
    min_init_context(&GATEWAY, 0);
    min_init_context(&SENSOR_NODE_1, 1);
    min_init_context(&SENSOR_NODE_2, 2);
    min_init_context(&CONTROL_NODE, 3);
    /* Configure parameters of an UART driver,
     * communication pins and install the driver */
    uart_config_t uart_config = {
        .baud_rate = 9600,
        .data_bits = UART_DATA_8_BITS,
        .parity = UART_PARITY_DISABLE,
        .stop_bits = UART_STOP_BITS_1,
        .flow_ctrl = UART_HW_FLOWCTRL_DISABLE,
        .source_clk = UART_SCLK_APB,
    };
    //Install UART driver, and get the queue.
    uart_driver_install(EX_UART_NUM, BUF_SIZE * 2, BUF_SIZE * 2, 20, &uart0_queue, 0);
    uart_param_config(EX_UART_NUM, &uart_config);

    //Set UART log level
    esp_log_level_set(TAG, ESP_LOG_INFO);
    //Set UART pins (using UART0 default pins ie no changes.)
    uart_set_pin(EX_UART_NUM, 4, 5, 18, 19); // TX : 4, RX : 5 
    output_io_create(M0);
    output_io_create(M1);
    input_io_create_without_interrupt(AUX);
    //Set uart pattern detect function.
    uart_enable_pattern_det_baud_intr(EX_UART_NUM, '+', PATTERN_CHR_NUM, 9, 0, 0);
    //Reset the pattern queue length to record at most 20 pattern positions.
    uart_pattern_queue_reset(EX_UART_NUM, 20);

    //Create a task to handler UART event from ISR
    xTaskCreate(uart_event_task, "uart_event_task", 8192, NULL, 12, NULL);
}

void uart_put(uint8_t *data, uint16_t len)
{
    uart_flush_input(EX_UART_NUM);
    uart_write_bytes(EX_UART_NUM,(char *)data, (size_t)len); 
}
void uart_set_callback(void *cb)
{
    p_uart_handle = cb; 
}